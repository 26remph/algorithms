def solution(n):
    if n == 1:
        return [1]

    ans = []
    for i in range(2, int(pow(n, 0.5) + 1)):
        while n % i == 0:
            ans.append(i)
            n /= i

    if n != 1:
        ans.append(int(n))

    return ans


if __name__ == "__main__":
    n = int(input())
    print(" * ".join(map(str, solution(n))))


