def is_first_num(num_1, num_2):

    # not swap
    checksum_1 = int(str(num_1) + str(num_2))
    checksum_2 = int(str(num_2) + str(num_1))

    # print(f'{checksum_1:,}')
    # print(f'{checksum_2:,}')

    if checksum_1 > checksum_2:
        return True
    else:
        return False

def get_bigdata(array, less):

    for i in range(1, len(array)):
        item_to_insert = array[i]
        j = i
        # while j > 0 and item_to_insert > array[j - 1]:
        while j > 0 and less(item_to_insert, array[j - 1]):
            array[j] = array[j - 1]
            j -= 1
        array[j] = item_to_insert
        # print(f'step {i}, sorted {i+1} elements: {array}')
    return array

